                    <h3><a href="#q201" name="q201">— What is Track2?</a></h3>
                    <div style="font-style:italic;">
                         Track2 is written with a 5-bit scheme (4 data bits + 1 parity), which allows for sixteen possible characters, which are the numbers 0-9, plus the six characters  : ; < = > ? .<br>
                        The selection of six punctuation symbols may seem odd, but in fact the sixteen codes simply map to the ASCII range 0x30 through 0x3f, which defines ten digit characters plus those six symbols. The data format is as follows:<br>
    <b>Start sentinel</b> — one character (generally ';')<br>
    <b>Primary account number (PAN)</b> — up to 19 characters. Usually, but not always, matches the credit card number printed on the front of the card.<br>
    <b>Separator</b> — one char (generally '=')<br>
    <b>Expiration date</b> — four characters in the form YYMM.<br>
    <b>Service code</b> — three digits. The first digit specifies the interchange rules, the second specifies authorisation processing and the third specifies the range of services<br>
    <b>Discretionary data</b> — as in track one<br>
    <b>End sentinel</b> — one character (generally '?')<br>
    <b>Longitudinal redundancy check (LRC)</b> — it is one character and a validity character calculated from other data on the track. Most reader devices do not return this value when the card is swiped to the presentation layer, and use it only to verify the input internally to the reader.<br><br>
                        <i>Example of Track2: 4131863756855927=17091010000018900000</i>

                    </div>
                    <hr>
                    <h3><a href="#q202" name="q202">— What is Track1?</a></h3>
                    <div style="font-style:italic;">
Track 1 is written with code known as DEC SIXBIT plus odd parity. The information on track 1 on financial cards is contained in several formats: <b>A</b>, which is reserved for proprietary use of the card issuer, <b>B</b>, which is described below, <b>C-M</b>, which are reserved for use by ANSI Subcommittee X3B10 and <b>N-Z</b>, which are available for use by individual card issuers:<br>
Track 1, <b>Format B:</b><br>
<b>Start sentinel</b> — one character (generally '%')<br>
<b>Format code="B"</b> — one character (alpha only)<br>
<b>Primary account number (PAN)</b> — up to 19 characters. Usually, but not always, matches the credit card number printed on the front of the card.<br>
<b>Field Separator</b> — one character (generally '^')<br>
<b>Name</b> — 2 to 26 characters<br>
<b>Field Separator</b> — one character (generally '^')<br>
<b>Expiration date</b> — four characters in the form YYMM.<br>
<b>Service code</b> — three characters<br>
<b>Discretionary data</b> — may include Pin Verification Key Indicator (PVKI, 1 character), PIN Verification Value (PVV, 4 characters), Card Verification Value or Card Verification Code (CVV or CVC, 3 characters)<br>
<b>End sentinel</b> — one character (generally '?')<br>
<b>Longitudinal</b> redundancy check (LRC) — it is one character and a validity character calculated from other data on the track.<br><br>
                      <i>Example of Track1: %B4430510072400715^GOLIASH/JOHN^180110111348100713000000</i><br>
