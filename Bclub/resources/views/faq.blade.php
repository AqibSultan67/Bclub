                    <h3><a href="#q203" name="q203">— What is Service Code?</a></h3>
                    <div style="font-style:italic;">
Service code values common in financial cards:<br>
<b>First digit</b><br>
&nbsp; &nbsp; &nbsp;1: International interchange OK<br>
&nbsp; &nbsp; &nbsp;2: International interchange, use IC (chip) where feasible<br>
&nbsp; &nbsp; &nbsp;5: National interchange only except under bilateral agreement<br>
&nbsp; &nbsp; &nbsp;6: National interchange only except under bilateral agreement, use IC (chip) where feasible<br>
&nbsp; &nbsp; &nbsp;7: No interchange except under bilateral agreement (closed loop)<br>
&nbsp; &nbsp; &nbsp;9: Test<br>
<b>Second digit</b><br>
&nbsp; &nbsp; &nbsp;0: Normal<br>
&nbsp; &nbsp; &nbsp;2: Contact issuer via online means<br>
&nbsp; &nbsp; &nbsp;4: Contact issuer via online means except under bilateral agreement<br>
<b>Third digit</b><br>
&nbsp; &nbsp; &nbsp;0: No restrictions, PIN required<br>
&nbsp; &nbsp; &nbsp;1: No restrictions<br>
&nbsp; &nbsp; &nbsp;2: Goods and services only (no cash)<br>
&nbsp; &nbsp; &nbsp;3: ATM only, PIN required<br>
&nbsp; &nbsp; &nbsp;4: Cash only<br>
&nbsp; &nbsp; &nbsp;5: Goods and services only (no cash), PIN required<br>
&nbsp; &nbsp; &nbsp;6: No restrictions, use PIN where feasible<br>
&nbsp; &nbsp; &nbsp;7: Goods and services only (no cash), use PIN where feasible <br><br>
                        <i>Examples: 101, 121, 201</i><br>
                    </div>
