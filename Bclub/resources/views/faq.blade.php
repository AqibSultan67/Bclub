                    <h3><a href="#q204" name="q204">— What is BIN?</a></h3>
                    <div style="font-style:italic;">
                        BIN is a bank identification number.<br>
                        The first six digits of a card number (including the initial MII digit) are known as the issuer identification number (IIN).<br>
                        These identify the card issuing institution that issued the card to the card holder.<br>
                        The rest of the number is allocated by the card issuer.<br><br>
                        <i>Examples: 414709, 601100, 542418</i><br>
